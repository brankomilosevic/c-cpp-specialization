#include<stdio.h>
int main(void)
{
    printf("She Walks in Beauty                     \n");
    printf("                            Lord Byron  \n");
    printf("                                        \n");
    printf("She walks in beauty, like the night     \n");
    printf("Of cloudless climes and starry skies;   \n");
    printf("And all that’s best of dark and bright  \n");
    printf("Meet in her aspect and her eyes;        \n");
    printf("Thus mellowed to that tender light      \n");
    printf("Which heaven to gaudy day denies.       \n");
    printf("                                        \n");
    printf("One shade the more, one ray the less,   \n");
    printf("Had half impaired the nameless grace    \n");
    printf("Which waves in every raven tress,       \n");
    printf("Or softly lightens o’er her face;       \n");
    printf("Where thoughts serenely sweet express,  \n");
    printf("How pure, how dear their dwelling-place.\n");
    printf("                                        \n");
    printf("And on that cheek, and o’er that brow,  \n");
    printf("So soft, so calm, yet eloquent,         \n");
    printf("The smiles that win, the tints that glow,\n");
    printf("But tell of days in goodness spent,     \n");
    printf("A mind at peace with all below,         \n");
    printf("A heart whose love is innocent.         \n");
    printf("                                        \n");
    return 0;
}
